import yaml
import pandas as pd

mixes = []
for i in range(5):
    for low_value in [1, 2, 3, 5, 10, 20]:
        mix = [0, 0, 0, 0, 0]
        rest = (100 - low_value) / 4
        for j in range(5):
            if i == j:
                mix[i] = low_value
            else:
                mix[j] = rest
        mixes.append(mix)

species = ["Burkholderia cepacia", "Bacillus spizizenii", "Escherichia coli", "Pseudomonas aeruginosa", "Staphylococcus aureus"]
config_dict = {}
for i in range(len(mixes)):
    config_dict[f"mix_{i + 1}"] = {key: item for key, item in zip(species, mixes[i])}

df = pd.DataFrame.from_dict(config_dict).T

print(yaml.dump(config_dict, default_flow_style=False, sort_keys=False))


